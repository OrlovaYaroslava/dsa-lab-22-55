# Задание 3.15 
# 1. Заполнить случайными нечетными числами одномерный массив, состоящий из N целочисленных элементов. 
# 2. Вывести в консоль элементы массива меньше 10. 
# 3. Умножить на 2 каждый четный индекс одномерного массива. 
# 4. Вывести полученный массив

# Читаем размер массива
N = int(input("Введите размер массива: "))

# Вводим N нечетных чисел вручную
arr = []
print("Введите", N, "нечетных чисел:")
while len(arr) < N:
    num = int(input())
    if num % 2 == 1: 
        arr.append(num)
    else:
        print("Число должно быть нечетным!")

# Вывод исходного массива
print(f"Исходный массив: {arr}")

# Числа меньше 10
small_numbers = [num for num in arr if num < 10]
print(f"Числа меньше 10: {small_numbers if small_numbers else 'Нет чисел < 10'}")

# Умножаем четные индексы на 2
for i in range(0, len(arr), 2):
    arr[i] *= 2

# Вывод модифицированного массива
print(f"Модифицированный массив: {arr}")
